#import "NSColor+Rainbows.h"


@implementation NSColor (Rainbows)

+ (NSColor *)prettyBlue {
	return [NSColor colorWithCalibratedRed:0.18 green:0.60 blue:1.0 alpha:1.0];
}

+ (NSColor *)smartPurple {
	return [NSColor colorWithCalibratedRed:0.68 green:0.25 blue:0.898 alpha:1.0];
}

@end
